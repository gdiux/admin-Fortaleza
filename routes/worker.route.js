/** =====================================================================
 *  WORKER ROUTER
=========================================================================*/
const { Router } = require('express');
const { check } = require('express-validator');

// HELPERS
const { validarCampos } = require('../middlewares/validar-campos');
const { validarJWT } = require('../middlewares/validar-jwt');

// CONTROLLERS
const { updateWorker, getWorkers, getWorkerId, excelWorker } = require('../controllers/worker.controller');

const router = Router();

/** =====================================================================
 *  GET WORKERS
=========================================================================*/
router.get('/', validarJWT, getWorkers);
/** =====================================================================
 *  GET WORKERS
=========================================================================*/

/** =====================================================================
 *  GET WORKER ID
=========================================================================*/
router.get('/:id', validarJWT, getWorkerId);
/** =====================================================================
 *  GET WORKER ID
=========================================================================*/

/** =====================================================================
 *  GET WORKER ID
=========================================================================*/
router.get('/excel/all', validarJWT, excelWorker);
/** =====================================================================
 *  GET WORKER ID
=========================================================================*/

/** =====================================================================
 *  PUT USER
=========================================================================*/
router.put('/:id', [
        validarJWT,
        validarCampos
    ],
    updateWorker
);
/** =====================================================================
*  PUT USER
=========================================================================*/



// EXPORT
module.exports = router;